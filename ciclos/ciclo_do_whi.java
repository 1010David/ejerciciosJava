/*El ciclo Do While se caracteriza por primero se ejecutan las instrucciones
 * y despues verifica la condicion es verdadera o no */
package ciclos;

public class ciclo_do_whi {
    public static void main(String[] args){
        
        int i=1;

        do{
            System.out.println(i);
            i++;
        }while(i<=10);




    }

}
